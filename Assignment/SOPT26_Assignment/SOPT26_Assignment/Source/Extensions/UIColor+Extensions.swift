import Foundation
import UIKit

extension UIColor {
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 237.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var royal: UIColor {
        return UIColor(red: 7.0 / 255.0, green: 59.0 / 255.0, blue: 163.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGrey: UIColor {
      return UIColor(white: 100.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGreyTwo: UIColor {
      return UIColor(white: 112.0 / 255.0, alpha: 0.5)
    }
    
    @nonobjc class var brownGrey: UIColor {
      return UIColor(white: 129.0 / 255.0, alpha: 1.0)
    }
}
